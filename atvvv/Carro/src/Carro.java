package atvvv.Carro.src;

public class Carro {

	String marca;
	String modelo;
	int ano;
	
	public Carro() {
		this.marca = "";
		this.modelo = "";
		this.ano = 0;
		
	}
	
	public Carro(String marca, String modelo, int ano) {
		
		this.marca = marca;
		this.modelo = modelo;
		this.ano = ano;
		
	}
	
}
